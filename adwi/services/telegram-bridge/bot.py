def _format_tests_status() -> str:
    """Show status of the most recent test job."""
    if _JOB_RUNNER is None:
        return "[error] Job runner not available."
    jobs = [j for j in _JOB_RUNNER.list_recent(20) if j.get("type", "").startswith("test-")]
    if not jobs:
        return "No test jobs run yet. Try /test_quick."
    j   = jobs[0]
    age = ""
    if j.get("end_time"):
        try:
            end = datetime.fromisoformat(j["end_time"])
            secs = (datetime.now() - end).total_seconds()
            age  = f" ({int(secs // 60)}m ago)"
        except Exception:
            pass
    lines = [
        f"Latest test: {j['type']}",
        f"  Status: {j['status']}{age}",
        f"  ID: {j['id']}",
    ]
    if j.get("status") in ("succeeded", "failed"):
        tail = _JOB_RUNNER.tail_log(j["id"], 12)
        if tail:
            lines.append("Last output:")
            lines.extend(f"  {l}" for l in tail.splitlines())
    lines.append(f"\nAll test jobs: /jobs  •  Full log: /job {j['id']}")
    return "\n".join(lines)


def _format_jobs_list() -> str:
    """List recent jobs."""
    if _JOB_RUNNER is None:
        return "[error] Job runner not available."
    jobs = _JOB_RUNNER.list_recent(10)
    if not jobs:
        return "No jobs yet."
    lines = ["Recent jobs (newest first):"]
    for j in jobs:
        start = (j.get("start_time") or "?")[:16]
        lines.append(f"  {j['status']:<10} {j['type']:<20} {start}  id={j['id']}")
    lines.append("\n/job <id> for details  •  /cancel <id> to cancel")
    return "\n".join(lines)


def _format_job_detail(job_id: str) -> str:
    """Show full job status + log tail."""
    if _JOB_RUNNER is None:
        return "[error] Job runner not available."
    if not job_id:
        return "Usage: /job <job-id>"
    j = _JOB_RUNNER.status(job_id)
    if not j:
        return f"Job not found: {job_id!r}"
    lines = [
        f"Job: {j['id']}",
        f"  Type:   {j.get('type', '?')}",
        f"  Status: {j.get('status', '?')}",
        f"  Start:  {(j.get('start_time') or '?')[:19]}",
        f"  End:    {(j.get('end_time') or '?')[:19]}",
        f"  Exit:   {j.get('returncode', '?')}",
    ]
    tail = _JOB_RUNNER.tail_log(job_id, 30)
    if tail:
        lines.append("\nLog (last 30 lines):")
        lines.extend(f"  {l}" for l in tail.splitlines())
    return "\n".join(lines)


def _do_cancel_job(job_id: str) -> str:
    if _JOB_RUNNER is None:
        return "[error] Job runner not available."
    if not job_id:
        return "Usage: /cancel <job-id>"
    ok = _JOB_RUNNER.cancel(job_id)
    return f"Cancelled: {job_id}" if ok else f"Could not cancel {job_id!r} — not running or not found."


def _do_capture(args: str, tg_token: str, chat_id: int) -> None:
    parts    = args.strip().split(maxsplit=1)
    if not parts:
        _send_reply(tg_token, chat_id,
                    f"Usage: /capture <type> <text>\n"
                    f"Types: {', '.join(sorted(_CAPTURE_TYPES))}")
        return
    cap_type = parts[0].lower()
    cap_text = parts[1].strip() if len(parts) > 1 else ""
    if cap_type not in _CAPTURE_TYPES:
        _send_reply(tg_token, chat_id,
                    f"Unknown type: {cap_type!r}\n"
                    f"Valid types: {', '.join(sorted(_CAPTURE_TYPES))}")
        return
    if not cap_text:
        _send_reply(tg_token, chat_id, f"Usage: /capture {cap_type} <text>")
        return
    cap_text = _sanitize_text(cap_text)
    result   = _run_quick([VENV_PY, ADWI_CLI, "/obsidian-capture", cap_type, cap_text], timeout=30)
    _send_reply(tg_token, chat_id, result or f"{cap_type.capitalize()} captured.")


def _do_repair_plan(tg_token: str, chat_id: int) -> None:
    # Quick syntax check
    syntax_ok = True
    try:
        py_compile.compile(ADWI_CLI, doraise=True)
    except py_compile.PyCompileError:
        syntax_ok = False

    # Git status summary
    try:
        gs_out = subprocess.run(
            ["git", "status", "--short"],
            capture_output=True, text=True, timeout=5, cwd=str(WORKSPACE),
        ).stdout.strip()
        dirty = len([l for l in gs_out.splitlines() if l.strip()])
    except Exception:
        gs_out = "(unknown)"
        dirty  = 0

    token_val = _make_token("repair", {})
    lines = [
        "Repair Plan",
        f"  adwi_cli.py syntax: {'✓ OK' if syntax_ok else '✗ FAIL'}",
        f"  Dirty files:        {dirty}",
        "",
        "Action: run adwi-self-heal (aider patching loop)",
        "This may modify source files. No git push.",
        "",
        f"Confirm: /repair_ok {token_val}",
        "Expires in 5 minutes.",
    ]
    _send_reply(tg_token, chat_id, "\n".join(lines))


def _do_repair_confirm(token_val: str, tg_token: str, chat_id: int) -> None:
    if not token_val:
        _send_reply(tg_token, chat_id, "Usage: /repair_ok <token>  (get token from /repair_plan)")
        return
    entry = _consume_token(token_val)
    if entry is None:
        _send_reply(tg_token, chat_id,
                    "Invalid or expired token. Run /repair_plan for a new one.")
        return
    if entry["action"] != "repair":
        _send_reply(tg_token, chat_id, "Token is for a different action.")
        return
    if _JOB_RUNNER is None:
        _send_reply(tg_token, chat_id, "[error] Job runner not available.")
        return
    heal_script = str(WORKSPACE / "adwi" / "bin" / "adwi-self-heal")
    job_id = _JOB_RUNNER.submit("repair", [heal_script])
    _send_reply(tg_token, chat_id,
                f"Repair job started.\nID: {job_id}\nCheck: /job {job_id}")


def _do_git_backup_plan(tg_token: str, chat_id: int) -> None:
    try:
        gs = subprocess.run(
            ["git", "status", "--short"],
            capture_output=True, text=True, timeout=5, cwd=str(WORKSPACE),
        ).stdout.strip()
        gl = subprocess.run(
            ["git", "log", "--oneline", "-1"],
            capture_output=True, text=True, timeout=5, cwd=str(WORKSPACE),
        ).stdout.strip()
    except Exception as exc:
        _send_reply(tg_token, chat_id, f"[error reading git state] {exc}")
        return

    dirty_lines = [l for l in gs.splitlines() if l.strip()]
    token_val   = _make_token("git_backup", {})
    lines = [
        "Git Backup Plan",
        f"  Last commit: {gl or '(none)'}",
        f"  Dirty files: {len(dirty_lines)}",
    ]
    for dl in dirty_lines[:8]:
        lines.append(f"    {dl}")
    if len(dirty_lines) > 8:
        lines.append(f"    … and {len(dirty_lines) - 8} more")
    lines += [
        "",
        "Action: adwi-git-backup (git add -A → commit → push)",
        "",
        f"Confirm: /backup_ok {token_val}",
        "Expires in 5 minutes.",
    ]
    _send_reply(tg_token, chat_id, "\n".join(lines))


