# ── Confirmation gate ─────────────────────────────────────────────────────────

_TOKEN_TTL  = 300   # seconds — 5 minutes
_PENDING: dict[str, dict] = {}   # token → {action, expires_at, meta}


def _make_token(action: str, meta: Optional[dict] = None) -> str:
    token = _secrets_module.token_hex(4)   # 8 hex chars
    _PENDING[token] = {
        "action":     action,
        "expires_at": time.time() + _TOKEN_TTL,
        "meta":       meta or {},
    }
    return token


def _consume_token(token: str) -> Optional[dict]:
    entry = _PENDING.pop(token, None)
    if entry is None or time.time() > entry["expires_at"]:
        return None
    return entry


def _expire_tokens() -> None:
    now     = time.time()
    expired = [t for t, e in list(_PENDING.items()) if now > e["expires_at"]]
    for t in expired:
        _PENDING.pop(t, None)


